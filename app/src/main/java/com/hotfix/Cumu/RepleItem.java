package com.hotfix.Cumu;

public class RepleItem {
    String content;
    String name;

    public RepleItem(String name, String content) {
        this.content = content;
        this.name = name;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public void setName() {
        this.name = name;
    }
}